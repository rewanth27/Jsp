package com.employee;
public class Employee 
{
  private int adhar;
  private String name;
  private String city;
  public Employee()
  {
	  
  }
  public int getAdhar()
  {
	  return adhar;
  }
  public String getName()
  {
	  return name;
  }
  public String getCity()
  {
	  return city;
  }
  public void setAdhar(int adhar)
  {
	  this.adhar=adhar;
  }
  public void setName(String name)
  {
     this.name=name;
  }
  public void setCity(String city)
  {
	  this.city=city;
  }
  
}
